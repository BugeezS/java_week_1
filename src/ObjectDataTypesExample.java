public class ObjectDataTypesExample {
    public static void  main(String[] args){
        //Boolean object
        Boolean isTrue = true;
        System.out.println("Boolean object example: " + isTrue);

        //Byte object
        Byte myByte = 127;
        System.out.println("Byte object examples: " + myByte);

        //Short object
        Short myShort = 32000;
        System.out.println("Short object example: " + myShort);

        //Integer object
        Integer myInt = 1234567890;
        System.out.println("Integer object example: " + myInt);

        //Long object
        Long myLong = 123456789012345L;
        System.out.println("Long object example: " + myLong);

        //Float object
        Float myFloat = 3.14f;
        System.out.println("Float object example: " + myFloat);

        //Double object
        Double myDouble = 3.141592653589793;
        System.out.println("Double object example: "  + myDouble );

        //Character object
        Character myChar = 'A';
        System.out.println("Character object example: " + myChar);

    }
}
